export function getRelatedApps(appId: string, limit: number = 3): App[] {
  const currentApp = apps.find(a => a.id === appId);
  if (!currentApp) return [];

  return apps
    .filter(app => app.id !== appId && app.categoria === currentApp.categoria)
    .slice(0, limit);
}
